package paquetecuatro;

public class Operacion {

    public int sumar(int v, int v2) {
        int suma = v + v2;
        return suma;
    }

    public int sumar(int v) {
        int suma = v + 10;
        return suma;
    }

    public int sumar(int v, int v2, int v3) {
        int suma = v + v2 + v3;
        return suma;
    }

}
