package paquetedos;

public class Operacion {

    public void sumar(int v, int v2) {
        int suma = v + v2;
        System.out.println(suma);
    }

    public void sumar(int v) {
        int suma = v + 10;
        System.out.println(suma);
    }

    public void sumar(int v, int v2, int v3) {
        int suma = v + v2 + v3;
        System.out.println(suma);
    }

}
